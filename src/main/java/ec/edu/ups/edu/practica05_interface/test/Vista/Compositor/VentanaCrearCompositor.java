    private void BtnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCalcularActionPerformed
    int cantidadComposiciones = Integer.parseInt(txtComposiciones.getText());
    int cancionestop100 = Integer.parseInt(txtTop.getText());
    double salario = Double.parseDouble(txtSalario.getText());

    double salarioFinal = calculateFinalSalario(cantidadComposiciones, cancionestop100, salario);

    txtSueldoFinal.setText(String.valueOf(salarioFinal));
    txtSueldoFinal.setEnabled(false);
}

private double calculateFinalSalario(int cantidadComposiciones, int cantidadCancionesTop100, double salario) {
    
    double salarioFinal = salario;

    if (cantidadComposiciones > 5) {
        salarioFinal += 300;
    }

    if (cantidadCancionesTop100 >= 1 && cantidadCancionesTop100 <= 3) {
        salarioFinal *= 1.1;
    } else if (cantidadCancionesTop100 >= 4 && cantidadCancionesTop100 <= 6) {
        salarioFinal *= 1.2;
    } else if (cantidadCancionesTop100 > 6) {
        salarioFinal *= 1.2;
    }

    return salarioFinal;
    }//GEN-LAST:event_BtnCalcularActionPerformed

