  private void calcularSalarioFinal(Compositor compositor) {
    int cantidadComposiciones = Integer.parseInt(txtComposiciones.getText());
    int cantidadCancionesTop100 = Integer.parseInt(txtTop.getText());
    double salario = compositor.getSalario();

    double salarioFinal = salario;

    if (cantidadComposiciones > 5) {
        salarioFinal += 300;
    }

    if (cantidadCancionesTop100 >= 1 && cantidadCancionesTop100 <= 3) {
        salarioFinal *= 1.1;
    } else if (cantidadCancionesTop100 >= 4 && cantidadCancionesTop100 <= 6) {
        salarioFinal *= 1.2;
    } else if (cantidadCancionesTop100 > 6) {
        salarioFinal *= 1.3;
    }

    compositor.setSalarioFinal(salarioFinal);
}

