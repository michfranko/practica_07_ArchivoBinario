    private void BtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarActionPerformed
   int codigo;

    try {
        String codigoText = txtCodigo.getText();

        if (!codigoText.isEmpty()) {
            codigo = Integer.parseInt(codigoText);
        } else {
            return;
        }
    } catch (NumberFormatException e) {
        return;
    }

    Cantante singer = cantanteControlador.buscar(codigo);

    if (singer != null) {
        List<Disco> discografia = singer.getDiscografia();
        int cantidadDiscos = (discografia != null) ? discografia.size() : 0;
        txtDiscos3.setText(String.valueOf(cantidadDiscos));

        txtNombre.setText(singer.getNombre());
        txtApellido.setText(singer.getApellido());
        txtNombreArtist.setText(singer.getNombreArtistico());
        txtGeneroMusical.setText(singer.getGeneroMusical());
        txtNacionalidad.setText(singer.getNacionalidad());
        txtEdad.setText(String.valueOf(singer.getEdad()));
        txtSalario.setText(String.valueOf(singer.getSalario()));
        txtGiras3.setText(String.valueOf(singer.getNumeroDeGiras()));
        txtSencillos3.setText(String.valueOf(singer.getNumeroDeSencillos()));
    } else {
         JOptionPane.showMessageDialog(this, mensajes.getString("mensajeBuscar"));
    }
    }//GEN-LAST:event_BtnBuscarActionPerformed

