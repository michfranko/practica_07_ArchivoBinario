    private void BtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAceptarActionPerformed
    int codigo;
    String nombre, apellido, nombreArtistico, nacionalidad, generoMusical;
    int edad, cantidadSencillos, cantidadGiras;
    double salario;

    try {
        String codigoText = txtCodigo.getText();

        if (!codigoText.isEmpty()) {
            codigo = Integer.parseInt(codigoText);
        } else {
            return;
        }
    } catch (NumberFormatException e) {
        return;
    }

    nombre = txtNombre.getText();
    apellido = txtApellido.getText();

    try {
        edad = Integer.parseInt(txtEdad.getText());
    } catch (NumberFormatException e) {
        return;
    }

    nombreArtistico = txtNombreArtist.getText();
    nacionalidad = txtNacionalidad.getText();
    generoMusical = txtGeneroMusical.getText();

    try {
        salario = Double.parseDouble(txtSalario.getText());
    } catch (NumberFormatException e) {
        return;
    }

    try {
        cantidadSencillos = Integer.parseInt(txtSencillos3.getText());
    } catch (NumberFormatException e) {
        return;
    }

    try {
        cantidadGiras = Integer.parseInt(txtGiras3.getText());
    } catch (NumberFormatException e) {
        return;
    }

     if (cantidadSencillos < 0) {
        return;
    }
    
    
    Cantante singer = new Cantante();
    List<Disco> discografia = singer.getDiscografia();
    int cantidadDiscos = (discografia != null) ? discografia.size() : 0;
    txtDiscos3.setText(String.valueOf(cantidadDiscos));

    singer.setCodigo(codigo);
    singer.setApellido(apellido);
    singer.setNombre(nombre);
    singer.setNacionalidad(nacionalidad);
    singer.setEdad(edad);
    singer.setNombreArtistico(nombreArtistico);
    singer.setGeneroMusical(generoMusical);
    singer.setSalario(salario);
    singer.setNumeroDeGiras(cantidadGiras);
    singer.setNumeroDeSencillos(cantidadSencillos);
    singer.setDiscografia(discografia);
    calcularSalarioFinal(singer);

    if (cantanteControlador.actualizar(singer)) {
 JOptionPane.showMessageDialog(this, mensajes.getString("mensajeActualizar"));
        LimpiarCampos();
    } else {
  JOptionPane.showMessageDialog(this, mensajes.getString("mensajeNOActualizar"));
    }
    }//GEN-LAST:event_BtnAceptarActionPerformed

